package main.java.cap3;

public class Variaveis {
    public static void main(String[] args) {
        int janeiro = 15000;
        int fevereiro = 23000;
        int marco = 17000;

        System.out.println("Gasto total: "+(janeiro+fevereiro+marco));
        System.out.println("Média de janeiro: "+(janeiro/30));
        System.out.println("Média de fevereiro: "+(fevereiro/30));
        System.out.println("Média de março: "+(marco/30));
    }
}
